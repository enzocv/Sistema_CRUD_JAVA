    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        usu = txtUsuario.getText();
        pass = txtPassword.getText();
        
        ClsNegocioUsuario usuario = new ClsNegocioUsuario();
        
        try {
            rs = usuario.LoginUsuario(usu, pass);
            boolean existe = false;
            
            while (rs.next()) {                
                if (usu.equals(rs.getString(4)) && pass.equals(rs.getString(5))) {
                    if ("Activo".equals(rs.getString(7))) {
                        JOptionPane.showMessageDialog(null,"Bienvenido al Sistema: " + rs.getString(2) +
                                                            " " + rs.getString(3) + "\n" +
                                                           "Nivel : " + rs.getString(6),"Login de Acceso",1);
                        
                        //enviar parametro al formulario principal
                        frm_Pricipal form_principal = new frm_Pricipal();
                        form_principal.show();
                        this.dispose();//cierra el login
                        form_principal.Apellido = rs.getString(3);
                        form_principal.Nombre   = rs.getString(2);
                        form_principal.Nivel    = rs.getString(6);
                        form_principal.Estado   = 1;
                        
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Lo sentimos:" + rs.getString(2) + " " + rs.getString(3) + "\n" +
                                                           "Te encuetras desactivado" + "\nPonte en contacto con el Administrador","Login de Acceso",1);
                    }
                
                }
                existe = true;
                break;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

